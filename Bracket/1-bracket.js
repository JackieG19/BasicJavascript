/* Note: 
Bracket notation is a way to get a character at a specific index within a string.
JavaScript does not start counting at 1 like humans do. 
They start at 0; this is referred to as Zero-based indexing.
*/

/* Using bracket notation to find the first character in the 
lastName variable and assign it to firstLetterOfLastName*/

var firstLetterOfLastName = "";
var lastName = "Lovelace";

firstLetterOfLastName = lastName[0];
